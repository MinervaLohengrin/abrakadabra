package model;

// Represents status of tasks;
// a task with a hard deadline will be labelled "HARD",
// while a task with a flexible deadline will be labelled "FLEXIBLE"

// source: https://docs.oracle.com/javase/tutorial/java/javaOO/enum.html
// represents the set of constants {HARD, FLEXIBLE}
public enum Status {
    HARD, FLEXIBLE;
}







